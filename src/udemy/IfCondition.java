package udemy;

public class IfCondition {
    public static void main(String[] args){
        int a=27;
        double b=34.3;
        if(a<b){
            System.out.println("a is less than b");
        }
        else if(a>b){
            System.out.println("a is greater than b");
        }
        else{
            System.out.println("a is equal to b");
        }
    }
}
