package com.example.helloworld;

public class EscapeCharacters {
    public static void main(String[] args){
        System.out.println("Hi, welcome to \"JAVA\"");  //display ""
        System.out.println("Hi, welcome to \tJAVA");    //tab space
        System.out.println("Hi, welcome to \nJAVA");    //new line
        System.out.println("Hi, welcome to \bJAVA");    //back space
        System.out.println("Hi, welcome to \fJAVA");    //form feed
        System.out.println("Hi, welcome to \rJAVA");    //carriage return
        System.out.println("File location: D\\New folder\\String.java");    //back slash
    }
}
